let price = 1901;

if (!(price >= 1000 && price <= 1900)){
    console.log(`Ціна товару ${price}`)
} else if (price <= 1000 || price >= 1900){
    console.log(`Ціна ${price}`)
} else {
    console.log(`Ціна в межах від 1000 до 19000`)
}
